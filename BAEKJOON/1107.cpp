#include<iostream>
#include<vector>
#include<cmath>
using namespace std;

int main(){
    vector<int> v;
    int n, m, in, a, b, c, d, e, f, ch;
    
    scanf("%d %d", &n, &m);

    for(int i = 0; i<m; i++){
        scanf("%d", &in);
        v[in] = 0;
    }

    n = abs(n - 100);

    if(!v.empty()){
        f = v.front();
        while(true){
            ch = a * 100000 + b * 10000 + c * 1000 + d * 100 + e * 10 + f;
        }
    }

}